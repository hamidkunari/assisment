     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required|min:3',
            'email' => 'required|unique:registers',
            'education' => 'required',
            'message' => 'required',
        ]);
        
        Register::create($request->post());

        return redirect()->route('index')->with('success','User has been created successfully.');
    }

